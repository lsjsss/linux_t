# while 循环
# while 条件表达式:
#     重复执行的代码
# 打印五次 hello world
times = 1
while times <= 5:
    print("hello world")
    times += 1  # times = times + 1
print("共计" + str(times-1) + "次")
# 练习: 使用 while 循环计算1~100的和





