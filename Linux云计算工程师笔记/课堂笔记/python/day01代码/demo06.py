# python中的数据类型: 数字
# int: 整型数
# float: 浮点数
# 布尔类型: True: 1  False: 0
# ===========================================
# ===========n进制  ->  10 进制的写法========
print(100)  # 十进制 0~9
print(0b101)  # 二进制 01
print(0xffddaa)  # 十六进制   0~9ABCEDF
print(0o776655)  # 八进制 0~7 权限
# ===========10 进制 -> n进制============
print(oct(100))  # 10 -> 8  oct(10进制数字)
print(hex(100))  # 10 -> 16 hex(10进制数字)
print(bin(100))  # 10 -> 2  bin(10进制数字)
